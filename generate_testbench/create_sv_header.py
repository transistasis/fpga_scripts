import os
import inspect
from   datetime import datetime

class CreateSvHeader:
  def __init__( self ):
    self.file_name = os.path.basename( __file__ )
    print( 'A CreateSvHeader object has been created' )

  def generate_sv_header( self, file_name, dut_name ):
    # Capture the current date and time
    self.timestamp = datetime.now()

    self.sv_header = '''\
    ################################################################################
    # Name        : tb_{dut_name}.sv
    # Author      :
    # Date        : {current_day}
    # Description :
    # Notes       :
    #
    # This file was autogenerated on {current_day} at {current_time} by the {file_name} script.
    ################################################################################
    '''.format( dut_name     = dut_name,\
                current_day  = self.timestamp.strftime("%B %d, %Y"),\
                current_time = self.timestamp.strftime("%H:%M:%S"),\
                file_name    = file_name )

    return inspect.cleandoc( self.sv_header )



if ( __name__ == '__main__' ):
  svh = CreateSvHeader()

  print( svh.generate_sv_header( svh.file_name, 'dut_name' ) )

